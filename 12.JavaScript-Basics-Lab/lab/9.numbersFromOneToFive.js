function solve() {
  // for --> looping through the numbers from 1 (starting point) to 5 (ending point)
  // let i = 1 --> initializes the loop counter i to the value of 1
  // i <= 5 --> the loop will continue to execute as long as i is greater than or equal to 5
  // i-- --> decrements i by 1 after each iteration
  for (let i = 1; i <= 5; i++) {
    console.log(i);
  }
}

solve();
